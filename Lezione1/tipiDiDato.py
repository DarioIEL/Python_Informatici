# Esistono i seguenti tipi di dato: String, Number (Integer, Floats), Boolean
print("Questa è una string")

print("2")  #Anche questa è una string
print(2) #Questo è un integer (è un numero intero)
print(2.0) #Questo è un float (è un numero con la virgola)
print(0.0000000001)

print(True) #Questo è un boolean
print(False)

#Questi sotto si chiamano operatori di confronto. Tutte le volte che uso un operatore di confronto produco un boolean. Gli operatori di confronto sono : > ; < ; <= ; >= ; != 
print(1 < 2) #True
print(3 > 5) #False

#Esempio
print("Voglio fare un calcolo matematico semplice")

print(1+2) #Integer
print(3-1) #Integer 
print(4*5) #Integer
print(10/3) #Float
#Il fatto che il linguaggio decida per noi il tipo di dato più adeguato determina il fatto che siamo di fronte ad un linguaggio debolmente tipizzato

print(2.5 + 1.3)
print(6.7 - 8.4)
print(4.2 * 2.3)
print(5.5 / 1.9)

print(3 * 0.9)

#Modulo %, anche chiamato Resto della Divisione
print( 14 % 2 ) #0
print( 13 % 2 ) # 13 / 2 = 6 col resto di 1
print(14 % 3 ) # 14 / 3 = 4 col resto di 2

# Altri operatori 
# ** elevazione a potenza
print(4 ** 6)
print(3 ** 2)

# // arrotonda all'intero inferiore
print (8.0 / 3)
print (8.0 // 3.0)
print (8.0 // 7.0)

#Usiamo le parentesi
print((5*2) + (8 * 3) - (10 / 2) * 4)